    public static final Font ICON_FONT = loadFont("MaterialIcons-Regular.ttf");

    private static Font loadFont(String resourceName) {
        try (InputStream inputStream = MaterialIcons.class.getResourceAsStream("/resources/fonts/" + resourceName)) {
            return Font.createFont(Font.TRUETYPE_FONT, inputStream);
        } catch (IOException | FontFormatException ex) {
            // Could not load resourceName
            Log.log(Level.SEVERE, "LoadFont", ex);
        }
        return UIManager.getDefaults().getFont("Label.font");
    }

    /**
     * Builds an image.
     *
     * @param iconCode the icon code.
     * @param size the size.
     * @return the image.
     */
    public static Image buildImage(char iconCode, float size) {
        return buildImage(iconCode, size, Color.BLACK);
    }

    /**
     * Builds an image.
     *
     * @param iconCode the icon code.
     * @param size the size.
     * @param color the size.
     * @return the image.
     */
    public static Image buildImage(char iconCode, float size, Color color) {
        String text = Character.toString(iconCode);
        return buildImage(text, ICON_FONT.deriveFont(size), color);
    }

    /**
     * Builds an icon.
     *
     * @param iconCode the icon code.
     * @param size the size.
     * @return the icon.
     */
    public static Icon buildIcon(char iconCode, float size) {
        return buildIcon(iconCode, size, Color.BLACK);
    }

    /**
     * Builds an icon.
     *
     * @param iconCode the icon code.
     * @param size the size.
     * @param color the size.
     * @return the icon.
     */
    public static Icon buildIcon(char iconCode, float size, Color color) {
        return new ImageIcon(buildImage(iconCode, size, color));
    }

    private static BufferedImage buildImage(String text, Font font, Color color) {
        JLabel icon = new JLabel(text);
        icon.setForeground(color);
        icon.setFont(font);
        Dimension dim = icon.getPreferredSize();
        int width = dim.width + 1;
        int height = dim.height + 1;
        icon.setSize(width, height);
        BufferedImage bufImage = new BufferedImage(width, height, BufferedImage.TYPE_INT_ARGB);
        Graphics2D g2d = bufImage.createGraphics();
        g2d.setRenderingHint(RenderingHints.KEY_TEXT_ANTIALIASING, RenderingHints.VALUE_TEXT_ANTIALIAS_ON);
        g2d.setRenderingHint(RenderingHints.KEY_FRACTIONALMETRICS, RenderingHints.VALUE_FRACTIONALMETRICS_ON);
        icon.print(g2d);
        g2d.dispose();
        return bufImage;
    }

